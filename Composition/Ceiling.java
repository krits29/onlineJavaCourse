public class Ceiling
{
    private int height;
    private String color;

    public Ceiling(int height, String color)
    {
        this.height = height;
        this.color = color;
    }

    public int getHeight()
    {
        return height;
    }

    public String getColor()
    {
        return color;
    }
}
